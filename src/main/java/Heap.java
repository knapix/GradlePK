public class Heap {
    public static void main(String[] args) {
        String brian = new String("Brian");
        System.out.println("0: " + brian);
        //concatination
        brian += "!";
        System.out.println("1: " + brian);

        String name = "Alexandra";
        int lenght = name.length();
        System.out.println(name + " contains " + lenght + " letters.");
    }
}
